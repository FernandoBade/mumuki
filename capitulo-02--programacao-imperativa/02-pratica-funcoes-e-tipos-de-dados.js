function quantoCusta (polegadas, memoria){
  return (polegadas * 60) + (memoria * 200) + 1000
}

// Exercícío 2
function meConvem (polegadas, memoria){
  return polegadas >= 32 && memoria >= 8 && quantoCusta(polegadas, memoria) < 6000
}

